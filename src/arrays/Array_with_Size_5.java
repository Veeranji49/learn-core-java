package arrays;

public class Array_with_Size_5 {

    public static void main(String[] args) {

        //Default values of Array
        int[] arr = new int[5];
        arr[0] = 1;
        arr[1] = 2;
        arr[2] = 3;
        arr[3] = 4;
        arr[4] = 5;
        for (int num : arr) {
            System.out.println(num + " ");
        }
    }
}

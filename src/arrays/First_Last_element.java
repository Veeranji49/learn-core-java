package arrays;

public class First_Last_element {

    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5, 6, 7, 8, 9};

        //displaying first element
        System.out.println(arr[0]);

        //displaying last element
        System.out.println(arr[arr.length - 1]);
    }
}

package operators;

public class Ternary_Operator {

    public static void main(String[] args) {

        // This is a shorthand for an if-else statement.

        // Ternary (?:)
        int a = 5;
        int b = 3;
        int result = (a > b) ? a : b; // result is 5
        System.out.println(result);

    }
}
